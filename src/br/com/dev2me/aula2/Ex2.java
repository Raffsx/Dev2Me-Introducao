package br.com.dev2me.aula2;

public class Ex2 {

	public static void main(String[] args) {
		int comp = 1;
		
		if (comp == 0) {
			System.out.println("valor igual a 0");
		}
		else if (comp == 1) {
			System.out.println("valor igual a 1");
		}
		else if (comp == 2) {
			System.out.println("valor igual a 2");
		}
		else if (comp == 3) {
			System.out.println("valor igual a 3");
		}
		else {
			System.out.println("nao eh 1 nem 0");
		}
	}
}
